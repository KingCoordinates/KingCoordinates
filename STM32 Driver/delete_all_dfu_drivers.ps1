

function Find-And-Delete-Driver($InfFileName) {
   Write-Host -NoNewline "`r`nLooking for"$InfFileName"... "
   Get-WindowsDriver -Online |
   where ClassName -Match "USB" |
   where OriginalFileName -Match $InfFileName |
   ForEach-Object { 
     Write-Host "Found" $_.Driver
     & $env:WINDIR\Sysnative\pnputil.exe /delete-driver $_.Driver /force}
}


